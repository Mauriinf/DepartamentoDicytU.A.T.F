						<b>RESUMEN</b><br/>
						<p class="excert">
La formulación del Plan Estratégico de Desarrollo Institucional 2013 – 2017 de la Universidad
Autónoma Tomás Frías, significa un hito trascendental en su vida institucional, porque se ha
decidido asumir con responsabilidad histórica el desafío de elaborar un instrumento técnico que
posibilite a nuestra Universidad cumplir con su misión de formar profesionales con el más alto nivel
académico.</p><br><p class="excert">
El presente documento tiene tres partes sustantivas: el Marco Metodológico, el Marco Referencial
Teórico – Conceptual y el Direccionamiento Estratégico Institucional, cada parte incluye capítulos
pertinentes a sus propósitos. Así, la primera tiene como objetivo presentar a los métodos y
procedimientos sobre los que se edifican, en general, todo el proceso de formulación del Plan
Estratégico de Desarrollo Institucional 2013 – 2017. Esta parte se considera fundamental por cuanto
constituye el soporte procedimental de todo el documento.</p><br><p class="excert">
La segunda parte sustantiva referida al Marco Referencial Teórico – Conceptual tiene contenidos
en relación a los aspectos históricos‐legales de nuestra Universidad, un análisis de las tendencias
de la Educación Superior en el presente siglo XXI y finalmente se establecen sustentaciones teóricoconceptuales
argumentadas por una opción del pluralismo de ideas que contrarrestan la imposición
de un determinado criterio hecho muy frecuente en la realidad del país.</p><br><p class="excert">
Finalmente, la tercera parte sustantiva tiene relación con el Direccionamiento Estratégico
Institucional. Aquí, se inicia con el Diagnóstico Institucional Sistémico, es decir, uno externo y otro
interno, que recogen los resultados de cada uno de los instrumentos aplicados para levantar
información primaria y secundaria, y que muestran a manera de radiografía el estado de situación
de la Gestión Académica y la Gestión Administrativa. De este trabajo, se deriva a la matriz de
fortalezas, oportunidades, debilidades y amenazas. El Marco Estratégico presenta, entre otros
aspectos, la Misión, la Visión y las Políticas Institucionales que tienen determinada correspondencia
con las Políticas Nacionales, cuidando la independencia de un propio criterio. La Elección
Estratégica, por su parte, muestra el Mapa Estratégico Institucional, los Objetivos de Desarrollo y el
Sistema de Indicadores. La Implementación Estratégica es uno de los puntos centrales, aquí se
presenta el Cuadro de Mando Integral con las perspectivas adaptadas a la Educación Superior e
incluyen las Acciones Estratégicas y la Estructura Organizacional. Finalmente, la Evaluación
Estratégica propone un Sistema de Información, Seguimiento y Evaluación, y una Rendición de
Cuentas que asegura una verificación y rectificación en la aplicación del plan.</p><br><p class="excert">
El presente documento, por tanto, es una propuesta de asumir una cultura de planificación
universitaria. Esto significa, pensar institucionalmente antes de decidir, es actuar con método, de
manera sistemática y de manera holística. Esta cultura de planificación universitaria es plantearse
políticas institucionales, es proyectarse y pensar en el futuro desde este presente que toca vivir a
la Universidad Autónoma Tomás Frías. Es una propuesta para creer en el conocimiento y en la
capacidad de transformación que tienen sus propios profesionales, para revertir este estado
universitario de quietud e indiferencia. En definitiva, esta herramienta de planificación institucional
universitaria es para pensar, construir y creer en el futuro. La disyuntiva es seguir en un mar de
improvisaciones y de errores, o se aprende a planificar las acciones institucionales orientadas en un
solo fin: “juntos construyendo nuestro futuro, hacia una Universidad con Excelencia”.</p><br>
